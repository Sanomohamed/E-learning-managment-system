    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = jTextField1.getText();
        String idNum = jTextField2.getText();
        String phoneNum = jTextField3.getText();
        String address = jTextField4.getText();
        String username = jTextField5.getText();
        String password = jTextField6.getText();

        if (name.isEmpty() || idNum.isEmpty() || phoneNum.isEmpty()
                || address.isEmpty() || username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter all details!");
        } else {
            Student student = new Student(name, idNum, address, phoneNum, username, password);
            if (system.signup(student)) {
                JOptionPane.showMessageDialog(this, "Successful!");
                new StudentLogin(system).setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Username already taken!");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

