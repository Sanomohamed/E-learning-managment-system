    private void jComboBox2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox2ActionPerformed

        try {
            String username = jComboBox1.getSelectedItem().toString().split(":")[0];
            Student std = system.getSudent(username);
            String code = jComboBox2.getSelectedItem().toString().split(":")[0];
            int semester = Integer.parseInt(jComboBox2.getSelectedItem().toString().split(":")[3]);
            ArrayList<String> grades = new ArrayList<>(Arrays.asList("A", "B", "C", "D", "E", "F", "NA"));
            jComboBox3.setSelectedIndex(grades.indexOf(std.getCourses(code, semester).getGrade()));
        } catch (Exception e) {
            System.out.println(e);
        }

    }//GEN-LAST:event_jComboBox2ActionPerformed

